public class Savings {

    String name, number, savingstype;
    double deposit;
    int years;

    public Savings(String name, String number, String savingstype, double deposit, int years) {
        this.name = name;
        this.number = number;
        this.savingstype = savingstype;
        this.deposit = deposit;
        this.years = years;
    }
}
